	var mapboxSatellite = L.tileLayer('https://api.mapbox.com/styles/v1/{id}/tiles/{z}/{x}/{y}?access_token={accessToken}', {
		attribution: 'Map data &copy; <a href="https://www.openstreetmap.org/">OpenStreetMap</a> contributors, <a href="https://creativecommons.org/licenses/by-sa/2.0/">CC-BY-SA</a>, Imagery © <a href="https://www.mapbox.com/">Mapbox</a>',
		maxZoom: 19,
		id: 'mapbox/satellite-streets-v11',
		tileSize: 512,
		zoomOffset: -1,
		accessToken: mapboxKey
	  })
	  
	var mapboxOutdoors = L.tileLayer('https://api.mapbox.com/styles/v1/{id}/tiles/{z}/{x}/{y}?access_token={accessToken}', {
		attribution: 'Map data &copy; <a href="https://www.openstreetmap.org/">OpenStreetMap</a> contributors, <a href="https://creativecommons.org/licenses/by-sa/2.0/">CC-BY-SA</a>, Imagery © <a href="https://www.mapbox.com/">Mapbox</a>',
		maxZoom: 19,
		id: 'mapbox/outdoors-v11',
		tileSize: 512,
		zoomOffset: -1,
		accessToken: mapboxKey
	  })
	  
	var openCycleMap = L.tileLayer('https://{s}.tile.thunderforest.com/cycle/{z}/{x}/{y}.png?apikey={apikey}', {
		attribution: '&copy; <a href="http://www.thunderforest.com/">Thunderforest</a>, &copy; <a href="https://www.openstreetmap.org/copyright">OpenStreetMap</a> contributors',
		apikey: thunderforestKey,
		maxZoom: 19
	  })
	  
	var osm = L.tileLayer('https://{s}.tile.openstreetmap.org/{z}/{x}/{y}.png', {
        maxZoom: 19,
        attribution: '&copy; <a href="https://openstreetmap.org/copyright">OpenStreetMap contributors</a>'
      }).addTo(map)
	  
    var gravel = L.tileLayer('http://www.wickedgravel.com/tiles/{z}/{x}/{y}.png', {
        minZoom: 5,
        maxZoom: 19,
        tms: false,
        attribution: 'Generated by TilesXYZ'
      }).addTo(map)
	  
	var baseMaps = {
		"OSM": osm,
		"Mapbox Satellite Streets": mapboxSatellite,
		"Mapbox Outdoors": mapboxOutdoors,
		"OpenCycleMap": openCycleMap,
	  }
	  
	var overlapMaps = {
		"Gravel Roads": gravel
	  }
	  
	L.control.layers(baseMaps, overlapMaps).addTo(map)